# Arithmetic operators _ performs simple calculations
x = 45
y = 21

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)  # modulus - returns the remainder after dividing

# Comparison operators
a = 56
b = 2
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b)  # equal to
print(a != b)  # not equal to

# Assignment operators - stores a value of a certain
z = 6

print(z)

num = 2
num += 10
print(num)

# logical operator
num1 = 5
num2 = 10
print(num1 < num2 and num1 == num2)
print(num1 < num2 or num1 == num2)
print(not(num1 < num2 and num1 == num2))
